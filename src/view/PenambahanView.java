    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        try{
            inputanKosongException();
            selectedValue = jenisDropdown.getSelectedItem().toString();
            
            if(action.equalsIgnoreCase("Update")){
                int jumlahSelisih = (Integer.parseInt(jumlahTambahInput.getText()) - Integer.parseInt(stokInput.getText()));
                o = new Obat(Integer.parseInt(idInput.getText()), namaInput.getText(), selectedValue, Integer.parseInt(jumlahTambahInput.getText()), Double.parseDouble(hargaInput.getText()));
                obatControl.updateDataObat(o);
                
                n = new Penambahan(jumlahSelisih, u, o);
                penambahanControl.insertDataPenambahan(n);
                JOptionPane.showMessageDialog(this, "Data Obat berhasil di Update");
                
            } else{
                o = new Obat(namaInput.getText(), selectedValue, Integer.parseInt(jumlahTambahInput.getText()), Double.parseDouble(hargaInput.getText()));
                obatControl.insertDataObat(o);
                
                n = new Penambahan(Integer.parseInt(jumlahTambahInput.getText()), u, o);
                penambahanControl.insertDataPenambahan(n);
                JOptionPane.showMessageDialog(this, "Data Obat baru berhasil ditambahkan");
            }
            setEditDeleteBtn(false);
            setComponent(true);
            action = "";
            clearText();
            showDataObat();
            
        } catch(InputanKosongException e){
            JOptionPane.showMessageDialog(this, e.message());
        } catch(Exception ex){
            JOptionPane.showMessageDialog(this, "Inputan harus angka");
        }
    }//GEN-LAST:event_saveBtnActionPerformed

