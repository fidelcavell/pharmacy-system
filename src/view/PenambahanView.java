    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        try{
            TableObat tObat = obatControl.showDataObat(searchInput.getText());
            if(tObat.getRowCount() == 0){
                clearText();
                JOptionPane.showMessageDialog(this, "Data Obat tidak ditemukan");
            } else{
                obatTable.setModel(tObat);
            }
            clearText();
            
        } catch(Exception e){
            System.out.println("Error : " + e.getMessage());
        }
    }//GEN-LAST:event_searchBtnActionPerformed

    private void obatTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_obatTableMouseClicked
        setEditDeleteBtn(true);
        setComponent(false);
        
        int clickedRow = obatTable.getSelectedRow();
        TableModel tableModel = obatTable.getModel();
        
        idInput.setText(tableModel.getValueAt(clickedRow, 0).toString());
        namaInput.setText(tableModel.getValueAt(clickedRow, 1).toString());
        jenisDropdown.setSelectedItem(tableModel.getValueAt(clickedRow, 2).toString());
        hargaInput.setText(tableModel.getValueAt(clickedRow, 3).toString());
        stokInput.setText(tableModel.getValueAt(clickedRow, 4).toString());
    }//GEN-LAST:event_obatTableMouseClicked

    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        int getAnswer = JOptionPane.showConfirmDialog(rootPane, "Apakah yakin ingin menghapus data obat ?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
        switch(getAnswer){
            case 0 : 
                // Jika Jawaban User : Yes
                try{
                    o = new Obat(Integer.parseInt(idInput.getText()), namaInput.getText(), selectedValue, Integer.parseInt(stokInput.getText()), Double.parseDouble(hargaInput.getText()));
                    obatControl.deleteDataObat(Integer.parseInt(idInput.getText()));
                    
                    int jumlahDelete = (0 - Integer.parseInt(stokInput.getText()));
                    n = new Penambahan(jumlahDelete, u, o);
                    penambahanControl.insertDataPenambahan(n);
                    
                    clearText();
                    showDataObat();
                    setEditDeleteBtn(false);
                
                } catch(Exception e){
                    System.out.println("Error : " + e.getMessage());
                }
                break;
            
            case 1 :
                // Jika Jawaban User : No
                break;
        }
    }//GEN-LAST:event_deleteBtnActionPerformed

